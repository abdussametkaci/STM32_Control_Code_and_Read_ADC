  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();

  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		/*
		contol code  -> 1000
		working code -> 1001
		*/
		
		HAL_ADC_Start_IT(&hadc1);
		
		if(!isControlCode){
			if (HAL_GPIO_ReadPin(GPIOB, bit0_Pin) == 1 &&
				  HAL_GPIO_ReadPin(GPIOB, bit1_Pin) == 0 &&
					HAL_GPIO_ReadPin(GPIOB, bit2_Pin) == 0 &&
					HAL_GPIO_ReadPin(GPIOB, bit3_Pin) == 0){
					
					isControlCode = 1;
			}
		
		} else if (!isWorkingCode){
			HAL_GPIO_TogglePin(GPIOA, led0_Pin | led1_Pin | led2_Pin | led3_Pin);
			HAL_Delay(1000);
			
			if (HAL_GPIO_ReadPin(GPIOB, bit0_Pin) == 1 &&
				  HAL_GPIO_ReadPin(GPIOB, bit1_Pin) == 0 &&
					HAL_GPIO_ReadPin(GPIOB, bit2_Pin) == 0 &&
					HAL_GPIO_ReadPin(GPIOB, bit3_Pin) == 1){
					
					isWorkingCode = 1;
			}
			
		} else{
			adcValueFourBit = adcToFourBit(12, adcValue);
			HAL_GPIO_WritePin(GPIOA, led0_Pin, ledState(adcValueFourBit, 0));
			HAL_GPIO_WritePin(GPIOA, led1_Pin, ledState(adcValueFourBit, 1));
			HAL_GPIO_WritePin(GPIOA, led2_Pin, ledState(adcValueFourBit, 2));
			HAL_GPIO_WritePin(GPIOA, led3_Pin, ledState(adcValueFourBit, 3));
			
		}
		
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
