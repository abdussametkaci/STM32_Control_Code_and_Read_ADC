/* USER CODE BEGIN 4 */
uint8_t adcToFourBit(uint8_t resolution, uint16_t adcValue){
	return 15 * adcValue / ((1 << resolution) - 1);
}

GPIO_PinState ledState(uint16_t adcValue, uint8_t bit){
	return (adcValue & (1 << bit)) != 0 ? GPIO_PIN_SET : GPIO_PIN_RESET;

}
