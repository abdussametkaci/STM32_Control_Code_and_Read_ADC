/**
* @brief This function handles EXTI line3 interrupt.
*/
void EXTI3_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI3_IRQn 0 */
	
	if(HAL_GPIO_ReadPin(GPIOA, button_Pin)){
		adcValueTemp = adcValueFourBit + 1;
		HAL_GPIO_WritePin(GPIOA, led0_Pin, ledState(adcValueTemp, 0));
		HAL_GPIO_WritePin(GPIOA, led1_Pin, ledState(adcValueTemp, 1));
		HAL_GPIO_WritePin(GPIOA, led2_Pin, ledState(adcValueTemp, 2));
		HAL_GPIO_WritePin(GPIOA, led3_Pin, ledState(adcValueTemp, 3));
		//delay 1 second approximately
		for(int i = 0; i < 2000; i++){
			for(int j = 0; j < 1000; j++){
			}
		}
	}
  /* USER CODE END EXTI3_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_3);
  /* USER CODE BEGIN EXTI3_IRQn 1 */

  /* USER CODE END EXTI3_IRQn 1 */
}

/**
* @brief This function handles ADC1 and ADC2 global interrupts.
*/
void ADC1_2_IRQHandler(void)
{
  /* USER CODE BEGIN ADC1_2_IRQn 0 */
	adcValue = HAL_ADC_GetValue(&hadc1);
	
  /* USER CODE END ADC1_2_IRQn 0 */
  HAL_ADC_IRQHandler(&hadc1);
  /* USER CODE BEGIN ADC1_2_IRQn 1 */
	HAL_ADC_Start_IT(&hadc1);
  /* USER CODE END ADC1_2_IRQn 1 */
}

